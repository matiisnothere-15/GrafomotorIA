const FiguraModelo: React.FC<{ canvasRef: React.RefObject<HTMLCanvasElement>; color?: string }> = ({ canvasRef, color = '#aaaaaa' }) => {
  useEffect(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext('2d');
    if (!ctx) return;

    ctx.beginPath();
    const [startX, startY] = modeloCirculo[0];
    ctx.moveTo(startX, startY);
    modeloCirculo.slice(1).forEach(([x, y]) => ctx.lineTo(x, y));
    ctx.closePath();
    ctx.strokeStyle = color;
    ctx.lineWidth = 4;
    ctx.stroke();
  }, [canvasRef, color]);

  return null;
};

export default FiguraModelo;
